def seed_benchmark_db(conn: sqlite3.Connection, items: list[BenchmarkItem]) -> list[int]:
    """Insert benchmark items as chunks. Returns list of chunk IDs."""
    # Ensure a project and document exist
    conn.execute(
        "INSERT OR IGNORE INTO projects (slug, name, path) VALUES (?, ?, ?)",
        ("benchmark", "Benchmark Project", "/benchmark"),
    )
    proj_id = conn.execute(
        "SELECT id FROM projects WHERE slug = 'benchmark'"
    ).fetchone()["id"]

    conn.execute(
        "INSERT OR IGNORE INTO documents (project_id, file_path, file_type, title) "
        "VALUES (?, ?, ?, ?)",
        (proj_id, "/benchmark/data.md", "markdown", "Benchmark Data"),
    )
    doc_id = conn.execute(
        "SELECT id FROM documents WHERE file_path = '/benchmark/data.md'"
    ).fetchone()["id"]

    chunk_ids = []
    for item in items:
        cursor = conn.execute(
            "INSERT INTO chunks (document_id, heading, content, chunk_index, token_count) "
            "VALUES (?, ?, ?, ?, ?)",
            (doc_id, item.title, item.content, len(chunk_ids), len(item.content.split())),
        )
        chunk_ids.append(cursor.lastrowid)

    conn.commit()
    return chunk_ids
